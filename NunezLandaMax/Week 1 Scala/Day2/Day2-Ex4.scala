package example

//4.Scala If-Else Statement
object ScalaExample{  
    def main(args:Array[String]){  
        var age:Int = 20;  
        if(age > 18){  //execute if age is bigger than 18
            println ("Age is greater than 18")  
        }
        else
        {
            println("Age is less or equal than 18") //execute if age less or equal than 18
        }  

    }
}  
